    private void btnSimpanTrxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanTrxActionPerformed
        // TODO add your handling code here:
        int jmlRow = 0;
        jmlRow = model.getRowCount();
        for (int row = 0; row < jmlRow;row++) {
             try {
                String kode =(String) cbNamaPelanggan.getSelectedItem();
                String kodeFix = kode.substring(0,4);
                transaksi trx = new transaksi();
                trx.setKode_faktur(txtFaktur.getText());              
                trx.setKode_barang((String) model.getValueAt(row, 0));
                trx.setId_pelanggan(kodeFix);
                trx.setHrg((String) model.getValueAt(row, 2));
                trx.setJml((String) model.getValueAt(row, 3));                
                transaksiServis.insert(trx);
                
            } catch (SQLException ex){
                Logger.getLogger(barangView.class.getName()).log(Level.SEVERE, null,ex);
                JOptionPane.showMessageDialog(this, ex);
            }            
             
        }
        JOptionPane.showMessageDialog(this, "Transaksi Penjualan Berhasil !");
    }//GEN-LAST:event_btnSimpanTrxActionPerformed

    
    
    
