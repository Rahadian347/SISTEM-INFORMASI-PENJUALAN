    private void cbNamaBarangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbNamaBarangItemStateChanged
        // TODO add your handling code here:
        String kode =(String) cbNamaBarang.getSelectedItem();
        String kodeFix = kode.substring(0,4);
        try {
            Statement ktB= koneksi.getConnection().createStatement();
            ResultSet rsKBr = ktB.executeQuery("select jumlah,harga from tabel_barang where kode_barang ='"+kodeFix+"'");
            while (rsKBr.next()){
                txtStok.setText(rsKBr.getString("jumlah"));
                txtHarga.setText(rsKBr.getString("harga"));
            }
        
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_cbNamaBarangItemStateChanged

    private void cbNamaPelangganItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbNamaPelangganItemStateChanged
        // TODO add your handling code here:
        String kode =(String) cbNamaPelanggan.getSelectedItem();
        String kodeFix = kode.substring(0,4);
        try {
            Statement ktB= koneksi.getConnection().createStatement();
            ResultSet rsKBr = ktB.executeQuery("select alamat,no_telp from pelanggan where id_pelanggan ='"+kodeFix+"'");
            while (rsKBr.next()){
                txtAlamat.setText(rsKBr.getString("alamat"));
                txtTelp.setText(rsKBr.getString("no_telp"));
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_cbNamaPelangganItemStateChanged

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String kode =(String) cbNamaBarang.getSelectedItem();
        String kodeFix = kode.substring(0,4);
        String namaBarang = kode.substring(5, kode.length()-0);
        int hrg = Integer.valueOf(txtHarga.getText());
        int jml = Integer.valueOf(txtJumlahBeli.getText());
        long subTot = hrg * jml;

        Object data[] = new Object[5];
        data [0] = kodeFix;
        data [1] = namaBarang;
        data [2] = txtHarga.getText();
        data [3] = txtJumlahBeli.getText();
        data [4] = subTot;
        
        model.addRow(data);
        this.hitungTotalBayar();
        this.kosongkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int delData = tblTrx.getSelectedRow();
        if (delData == -1){
        return;
        } else {
            model.removeRow(delData);
            this.hitungTotalBayar();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

